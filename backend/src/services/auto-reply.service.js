/**
 * Logic-based Auto-Responder (No AI)
 * Provides instant, reliable answers for common phrases.
 */
export function autoReply(message) {
    const text = message.toLowerCase().trim();

    // 0. Проверка на вежливость (если сообщение слишком короткое и нет приветствия)
    const minGreetings = ["привет", "здравствуй", "ку", "хай", "прив", "добрый", "здорово", "салам"];
    if (text.length <= 2 && !minGreetings.some(w => text.includes(w))) {
        return "Привет! Для начала давай поздороваемся? 🙂";
    }

    // 1. Приветствия (Множество вариаций)
    const greetings = ["привет", "hello", "hi", "здарова", "салам", "здравствуй", "ку", "прив", "добрый день", "добрый вечер", "доброе утро", "хай", "хелло", "салют", "приветствую", "дратути", "вечер в хату", "здорово", "приветики", "здравия желаю"];
    if (greetings.some(w => text.includes(w))) {
        return random([
            "Привет! Чем я могу вам помочь?",
            "Здравствуйте! Я на связи.",
            "Привет 🙂 Что вас интересует?",
            "Рада вас видеть! Есть вопросы по проекту?",
            "Привет-привет! Как ваши успехи?",
            "Здравствуй! Готова к работе."
        ]);
    }

    // 2. Статус / Дела
    const statusQuery = ["как дела", "как ты", "что нового", "как жизнь", "how are you", "все ок", "все хорошо", "дела как", "че каво", "как оно", "норм?", "все живы?"];
    if (statusQuery.some(w => text.includes(w))) {
        return random([
            "У меня всё отлично, работаю в штатном режиме!",
            "Все системы в норме, готова помогать 🙂",
            "Прекрасно! А как ваши дела в программировании?",
            "Иду по графику, ошибок не обнаружено.",
            "Все функции активны, жалоб нет."
        ]);
    }

    // 3. Подтверждение / Ок / Сейчас (Много сленга)
    const confirmation = ["ок", "сейчас", "понял", "хорошо", "ладно", "понятно", "окей", "окэй", "сичас", "ща", "сию минуту", "принято", "база", "согл", "plus", "+", "гг", "wp", "roger", "понял-принял", "ясн", "ясно"];
    if (confirmation.some(w => text.includes(w))) {
        return random([
            "Хорошо, поняла!",
            "Договорились, жду продолжения.",
            "Принято. Что-то еще?",
            "Ок, я на связи.",
            "Поняла вас.",
            "Отлично.",
            "Заметано!"
        ]);
    }

    // 4. Технические проблемы (Ошибка, сломалось, не работает)
    const issues = ["не работает", "ошибка", "баг", "сломалось", "проблема", "лагает", "тупит", "вылетает", "не открывается", "черный экран", "глючит", "виснет", "не грузит", "error", "bug", "fault", "failed", "крашнулось", "не пускает", "не заходит", "тормозит"];
    if (issues.some(w => text.includes(w))) {
        return "Похоже, возникла техническая заминка. Пожалуйста, опишите шаги, которые привели к ошибке, или пришлите скриншот консоли (F12). Я постараюсь помочь разобраться!";
    }

    // 5. Кто ты / Имя
    const identity = ["кто ты", "что ты", "как тебя зовут", "ты кто", "что это за бот", "твое имя", "представься", "расскажи о себе", "кто ты такая"];
    if (identity.some(w => text.includes(w))) {
        return "Я — Мита, ваш персональный ассистент в EliteHeat. Я помогаю с вопросами по коду, навигации по сайту и технической поддержкой.";
    }

    // 6. Помощь / Как сделать
    const helpQuery = ["помоги", "нужна помощь", "как сделать", "не понимаю", "подскажи", "help", "хелп", "вопрос", "нужен совет", "выручай", "че делать", "как быть", "хелпните"];
    if (helpQuery.some(w => text.includes(w))) {
        return "Конечно! Сформулируйте ваш вопрос точнее: это касается кода Python, работы сайта или чего-то другого?";
    }

    // 7. Благодарности
    const thanks = ["спасибо", "благодарю", "спс", "thx", "thank", "от души", "лучший", "красава", "спасибочки", "сяб", "пасиб", "сенкс", "респект"];
    if (thanks.some(w => text.includes(w))) {
        return random([
            "Всегда пожалуйста! Рада быть полезной 🙂",
            "Не за что! Если будут еще вопросы — пишите.",
            "Рада помочь! Удачи в делах!",
            "Обращайтесь в любое время!",
            "Для этого я и здесь!"
        ]);
    }

    // 8. Оплата / Цена
    const price = ["цена", "сколько стоит", "купить", "оплата", "донат", "стоимость", "прайс", "money", "price", "buy", "как купить", "ценник", "прайс-лист", "дешево", "скидка"];
    if (price.some(w => text.includes(w))) {
        return "Информацию о тарифах и оплате вы можете найти в личном кабинете или разделе 'Услуги'. Если есть конкретный вопрос — спрашивайте!";
    }

    // 9. Прощания
    const goodbye = ["пока", "до свидания", "до встречи", "бб", "давай", "удачи", "прощай", "bye", "goodbye", "спокойной ночи", "ббшка", "ушел", "оффаюсь", "до завтра", "споки", "покеда"];
    if (goodbye.some(w => text.includes(w))) {
        return random([
            "До связи! Удачи!",
            "Пока! Буду ждать новых вопросов.",
            "Всего доброго! Не забывайте сохранять код.",
            "До встречи!",
            "Удачи в разработке!",
            "На связи!"
        ]);
    }

    // 10. Обучение / Python / Код
    const codingQuery = ["python", "пайтон", "питон", "код", "программирование", "скрипт", "функция", "переменная", "цикл", "массивы", "библиотека", "ошибка в коде", "как написать", "syntax", "code"];
    if (codingQuery.some(w => text.includes(w))) {
        return random([
            "Я обожаю Python! Какой именно аспект вас интересует? Синтаксис, библиотеки или конкретная ошибка?",
            "Код — это моя стихия. Присылайте фрагмент, разберемся вместе.",
            "В программировании главное — практика. Есть трудности с каким-то заданием?",
            "Python отличный выбор. Могу подсказать по функциям, циклам или структурам данных."
        ]);
    }

    // 11. Навигация / Разделы сайта
    const navQuery = ["где найти", "как перейти", "страница", "раздел", "меню", "личный кабинет", "настройки", "профиль", "рейтинг", "задания", "уроки", "курсы"];
    if (navQuery.some(w => text.includes(w))) {
        return "Вся навигация находится в боковом меню или верхней панели. Основные разделы: 'Задания', 'Профиль' и 'Рейтинг'. Что именно вы потеряли?";
    }

    // 12. Реакции / Эмоции
    const reactions = ["ахах", "лол", "lol", "хаха", "смешно", "жесть", "круто", "топ", "класс", "ого", "wow", "nice", "прекрасно", "ужасно"];
    if (reactions.some(w => text.includes(w))) {
        return random([
            "Согласна!",
            "Именно так 🙂",
            "Рада, что вы так считаете.",
            "Абсолютно точно!",
            "Понимаю ваши эмоции."
        ]);
    }

    // 13. Время / Когда
    const timeQuery = ["когда", "сколько времени", "долго", "когда будет", "дата", "время", "срок", "дедлайн"];
    if (timeQuery.some(w => text.includes(w))) {
        return "Время — ценный ресурс. Большинство обновлений и проверок происходит в течение суток. Следите за уведомлениями!";
    }

    // 14. Согласие / Несогласие
    const agreement = ["да", "нет", "неа", "конечно", "наверное", "возможно", "согласен", "не согласен", "верно", "неправильно"];
    if (agreement.some(w => text.includes(w))) {
        return "Хорошо, я учту вашу позицию. Продолжим обсуждение?";
    }

    // 7. ЛЮБОЙ ДРУГОЙ ВОПРОС (Универсальный)
    return random([
        "Поняла.",
        "Хорошо.",
        "Я тебя поняла.",
        "Ок.",
        "Принято."
    ]);
}

function random(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}
