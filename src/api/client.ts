import axios, { AxiosInstance, AxiosError, InternalAxiosRequestConfig } from 'axios';

// –°–æ–∑–¥–∞–µ–º –±–∞–∑–æ–≤—ã–π API –∫–ª–∏–µ–Ω—Ç
const apiClient: AxiosInstance = axios.create({
  baseURL: import.meta.env.VITE_API_URL || 'http://localhost:3000',
  timeout: 10000,
  headers: {
    'Content-Type': 'application/json',
  },
  withCredentials: true, // –î–ª—è —Ä–∞–±–æ—Ç—ã —Å cookies/sessions
});

// –ò–Ω—Ç–µ—Ä—Ü–µ–ø—Ç–æ—Ä –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è —Ç–æ–∫–µ–Ω–∞ –∫ –∑–∞–ø—Ä–æ—Å–∞–º
apiClient.interceptors.request.use(
  (config: InternalAxiosRequestConfig) => {
    // –ü–æ–ª—É—á–∞–µ–º —Ç–æ–∫–µ–Ω –∏–∑ localStorage
    const token = localStorage.getItem('authToken');
    
    if (token && config.headers) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    
    console.log(`üöÄ API Request: ${config.method?.toUpperCase()} ${config.url}`);
    return config;
  },
  (error: AxiosError) => {
    console.error('‚ùå Request Error:', error);
    return Promise.reject(error);
  }
);

// –ò–Ω—Ç–µ—Ä—Ü–µ–ø—Ç–æ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ—Ç–≤–µ—Ç–æ–≤
apiClient.interceptors.response.use(
  (response) => {
    console.log(`‚úÖ API Response: ${response.config.url}`, response.status);
    return response;
  },
  (error: AxiosError) => {
    console.error('‚ùå Response Error:', error.response?.status, error.message);
    
    // –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏
    if (error.response?.status === 401) {
      console.warn('‚ö†Ô∏è Unauthorized - redirecting to login');
      localStorage.removeItem('authToken');
      // –ú–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å —Ä–µ–¥–∏—Ä–µ–∫—Ç –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É –ª–æ–≥–∏–Ω–∞
      // window.location.href = '/login';
    }
    
    // –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ —Å–µ—Ä–≤–µ—Ä–∞
    if (error.response?.status === 500) {
      console.error('üî• Server Error:', error.response.data);
    }
    
    return Promise.reject(error);
  }
);

export default apiClient;
